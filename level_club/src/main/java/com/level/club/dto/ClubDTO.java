package com.level.club.dto;

public class ClubDTO {
	private int club_num;
	private int cate_num;
	private int leader_num;
	private String club_name;
	private String club_info;
	private String club_open_date;
	private String club_main_image;
	
	public int getClub_num() {
		return club_num;
	}
	public void setClub_num(int club_num) {
		this.club_num = club_num;
	}
	public int getCate_num() {
		return cate_num;
	}
	public void setCate_num(int cate_num) {
		this.cate_num = cate_num;
	}
	public int getLeader_num() {
		return leader_num;
	}
	public void setLeader_num(int leader_num) {
		this.leader_num = leader_num;
	}
	public String getClub_name() {
		return club_name;
	}
	public void setClub_name(String club_name) {
		this.club_name = club_name;
	}
	public String getClub_info() {
		return club_info;
	}
	public void setClub_info(String club_info) {
		this.club_info = club_info;
	}
	public String getClub_open_date() {
		return club_open_date;
	}
	public void setClub_open_date(String club_open_date) {
		this.club_open_date = club_open_date;
	}
	public String getClub_main_image() {
		return club_main_image;
	}
	public void setClub_main_image(String club_main_image) {
		this.club_main_image = club_main_image;
	}
}
